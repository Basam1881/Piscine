int		ft_str_is_alpha(char *str)
{
	int count;

	count = 1;
	while (*str)
	{
		if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && str[count] <= 'Z'))
		{
		}
		else
		{
			count = 0;
		}
		str++;
	}
	return (count);
}
